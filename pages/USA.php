		<h2>MY SEMESTER WITH UNCLE SAM</h2>
		<p>Do you know where the term YOLO came from? It certainly isn't original. Brings me back 
		to Dead Poet's Society where we see Robbin Williams' enthusiastic persuasion to his students 
		to seize the day! Carpe Diem! That was the YOLO back in the day. This concept does have its 
		flaws. However, I decided to run with it during the course of my stay in the US. And to be 
		honest, there were no regrets.</p>
		<p>I stepped off the plane in Chattanooga in the middle of winter. I'm shaking with excitement 
		at the thought of seeing and touching the very real snow outside the airport. Everything here 
		was different. The sink taps, the toilets, the gigantic fruits and food portions. And of course, 
		the people, whom I could barely understand. The culture shock was real. But I decided, the time 
		I would be spending here, it shall NOT go to waste. I opened myself up to new experiences every 
		single day. Disregarded plans that I usually meticulously kept to. And did all the crazy things 
		I would have never caught myself doing before. It was fun! At most times at least. (Other times, 
		it was less fun and more bordering on insanity...)</p>
		<p>You know that moment that dawns on you when you realize that you've signed yourself up for 
		something truly absurd? That moment came when I was told to grab a box, make a shelter with it, 
		and sleep in said box, outdoors, the entire night. Let me first rewind so you'll understand the 
		whole story. </p>
		<p>My church had an event to educate us on the homeless people that were living alongside us in 
		Chattanooga. We had an ex-homeless man share his story with us. It was difficult to hear how he 
		survived without shelter and income, not knowing when his next meal would be, or where he would 
		lay his head for the night. We were prepared for a sleepover at the church at the end of the program. 
		Little did we know that we were going to be having that sleepover, outside of the church. In the 
		middle of winter. In boxes. That we had to make to protect ourselves against the wind. Did I mention 
		that it was in the middle of winter? Because that is the essential detail when I describe the 
		suffering we went through trying to fall asleep that sordid night. I tried my best to not die of 
		cold. All my very Asian body could do to combat the biting chill was to shiver involuntarily 
		throughout the night. I did NOT sleep a wink. Which was just as well, since we were all rudely 
		awakened at 6 am by a cop. The screaming sirens and police announcement telling us to vacate the 
		premises immediately, woke us all up groggy. My adrenaline rush coupled with my lack of sleep gave 
		me temporary delirium and I ran exhilarated to the policemen to interview them! I kid you not! I 
		wanted to interview them because they were the first cops I've met since I touched down in US. And 
		you know what the crazy thing is? They complied! And participated! I even took a picture with one of them! </p>
		<p>So that was Carpe Diem incident number one. Allow me to walk you through incident number 2; Smoky Mountains.</p>
		<p>I don't hike. Never. Know why? Cause I love being alive. Hiking increases chance of death by a 
		substantial percent, I'm sure. Hence with all logic behind me, I avoid it. BUT. We are in US after 
		all. I got an email from my teacher about a hike up Smoky Mountains in a couple weeks. Pumped with 
		adrenaline to do anything and everything, I typed out "I would LOVE to!!" and hit SEND. And the time 
		came. I frantically packed whatever outdoorsy equipment and clothing I had lying around and hopped 
		on the bus. We reached the campsite, cooked, and went to bed, ready for the hike the next morning. </p>
		<p>It was 6am, and I was insanely sleepy. But the clanging... What on earth is that racket going on 
		outside the tent?? I dragged myself out swaddled with my warm blankets. Hating the source of the 
		clanging. "This moron making all this noise is asking to be murdered this morning", I muttered 
		to myself. And my teachers came into focus. They were clanging pots and pans and sounding the 
		bus horn to scare away bears! Bears! I told you hiking is deadly! We were almost mauled by bears! 
		Great way to start the day. </p>
		<p>Ate breakfast and the climb began. And continued. And it kept going! I did NOT do my research 
		before signing up for this. What I thought to be a two hour hike turned out to be something that 
		we kept at for TEN. Straight. Hours. Smoky mountains is over six thousand feet high. We hiked around 
		20 kilometers upwards and that same distance down. Boy was this one of the most challenging 
		expeditions ever. The second I reached the top, I immediately sat on my bottom, downed a whole 
		bottle of water and attacked my bagel with such gusto, Bear Grylls would be proud. The scenery 
		though. As I sat and munched. It truly is, worth risking death for. </p>
		<p>So, two Carpe Diem moments from yours truly. Amazing adventures appear where you least expect 
		it. And these are the ones worth taking a risk for. As Robin Williams so bluntly put "each of us 
		is one day going to stop breathing, turn cold, and die". So we should make the most of our 
		wonderful lives and this amazing world. While we can.</p>
		
		
