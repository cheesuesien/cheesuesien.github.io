		<p>Coming from Malaysia, I have extremely high tolerance for spicy food. Or so I thought. We 
		were at a local eatery in Bali and I heaped cili padi onto my plate as I usually do during my 
		meals. I was not prepared for the impending trauma. It was the spiciest thing my tongue had 
		ever come into contact with. I teared up the second the food entered my mouth. Just fire! 
		Fire on my tongue! I was panting for water, tea, anything to take the pain away! When the 
		flames waned, I looked up at my friends who were all in the same state I was in. We laughed 
		so much, but learned a lesson to never touch cili padi again in Bali. We were no longer in 
		Malaysia. Bali had a completely different level of spiciness.</p>
