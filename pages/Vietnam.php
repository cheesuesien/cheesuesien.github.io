		<h2>Vietnam</h2>
		<p>If you were to ask me "What was the thing that struck you most about Vietnam?", my 
		answer would be: the ubiquitous sea of motorbikes. </p>
		<div class="pic">
			<img src="../images/Vietnam1.jpg"/>
		</div>
		<p>If you were crossing the road, they resemble a wave of orcs in armor coming straight 
		at you. Crossing roads here bore semblance to going to war. You'd need a truckload of 
		guts. (And possibly a dip in the river Styx for impenetrable skin.) I attempted to 
		cross the roads here, alone. Which was an utterly dense move because, to summon the 
		power to halt the ocean of motorcyclists coming at you, a crowd is vital. Half of 
		the road was relatively empty at the time. I managed to cross to the middle. It was 
		there that my courage failed and I stopped, terrified, with motorists zooming around 
		me and missing my very vulnerable self by mere inches. I was sandwiched! Somehow, I 
		was able to dodge my way around those machines of murder and come out alive. Lesson 
		learnt: Never try to cross Vietnamese roads alone. Ever.</p>
		<p>The other parts of my trip in Ho Chi Minh were not as petrifying. Here are some memorable highlights:</p>
		<p>Being welcomed with an enormous Vietnamese baguette. They were comically larger 
		than our heads. And extremely spicy! Tears were shed in honour of this devilishly delicious treat. </p>
		<div class="pic">
			<img src="../images/Vietnam2.jpg"/>
		</div>
		<p>Vietnam's tiny tables and chairs. They litter the sidewalks of Vietnam, and are 
		simply adorable. What made it really amusing was when they were occupied by obese, 
		heavyset men. The image was so absurd that I could not hold back laughter. Those tiny 
		pieces of furniture swaying precariously beneath the massive men, who were not the 
		slightest bit perturbed, were a sight to behold.</p>
		<div class="pic">
			<img src="../images/Vietnam3.jpg"/>
		</div>
		<p>The most treasured part of my trip was the chance to visit one of the major battle 
		sites of the Vietnam War, the Cu Chi Tunnels. The tour gave us a first-hand experience 
		in seeing the weapons, forts and tunnels the Vietnamese built in 1954 to defend themselves 
		against the American soldiers for 19 years. </p>
		<div class="pic">
			<img src="../images/Vietnam4.jpg"/>
		</div>
		<p>As the tour guide waxed eloquent about the Vietnam War, I found myself thinking, "Yawn, not another history lesson!"<p>
		<p>As, it turns out, I was gladly mistaken.</p>
		<p>Slowly, but surely, the stories began to enchant me. Tale after tale was told of the 
		pure genius of the Vietnamese soldiers and their brilliant and unorthodox ways of outsmarting 
		their American foes.</p>
		<p>For instance, did you know they invented backwards sandals? They would run one way and 
		their footprints would appear to be heading in the opposite direction. This was to confuse 
		the enemy tracking their footsteps and prevent them from finding the entrance to their secret 
		tunnels. Imagine the American soldiers scratching their helmeted heads in puzzlement! An 
		ingenious idea indeed!</p>
		<p>The tour guide continued by telling and showing us a number of different booby traps that the 
		Vietnamese set to catch and kill their enemies. Many of these involved sharp spikes and long, 
		cruel nails. Simple and crude, but scarily effective. I shuddered. The ruthlessness of war is 
		a sobering thought.</p>
		<div class="pic">
			<img src="../images/Vietnam5.jpg"/>
			<img src="../images/Vietnam6.jpg"/>
		</div>
		<p>Another thing that fascinated me was seeing the actual tunnels.</p>
		<p>I was blown away with the amount of will power and resilience demonstrated by the Vietnam 
		troops in building the Cu Chi Tunnels, their primary defense structure in the Vietnam War. </p>
		<div class="pic">
			<img src="../images/Vietnam7.jpg"/>
			<img src="../images/Vietnam8.jpg"/>
		</div>
		<p>Over the course of 20 years, they built their homes within their underground fortress which 
		was essentially a human anthill that protected them against the aerial attacks of the American troops. </p>
		<div class="pic">
			<img src="../images/Vietnam9.jpg"/>
		</div>
		<p>The whole tunnel stretched as far as 200 kilometers. Two. Hundred. Kilometers. That is the 
		distance from KL to Perak! And they did it with the most rudimentary of excavation equipment: 
		the shovel. Besides that, a basket was also used to pile up soil to be disposed of into a nearby 
		river; another tactic to conceal their digging spots from the US army. </p>
		<div class="pic">
			<img src="../images/Vietnam10.jpg"/>
		</div>
		<p>More than 43,000 Vietnamese died defending the Cu Chi Tunnels. And I got to crawl through 
		them...like one of the Vietnamese soldiers. Let me just say, this is the sole reason the Vietnamese 
		are so skinny. Need to shed some pounds? Just crawl through that 6 meter long tunnel in a hunched 
		and crouched position and you will emerge from the ground aching and drenched in sweat. It was both 
		a gruelling and thrilling experience. </p>
		<div class="pic">
			<img src="../images/Vietnam11.jpg"/>
		</div>
		<p>Vietnam is a country with many hidden gems. Their rich and intense past left its raw mark on 
		the people here. Endurance against hardship and intrinsic diligence are just a few of their 
		qualities that I admire and will strive to imitate. The history and culture here is potent. It 
		seeps through their lifestyles, their food, and their architecture. The ghost of the past lives 
		on in Ho Chi Minh City permeating into this rapidly developing city, unveiling to visitors an 
		unadulterated and eye-opening experience of two contrasting worlds. A seamless blend of the past 
		and the present.</p>
