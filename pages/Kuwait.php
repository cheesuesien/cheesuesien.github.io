		<p>This was my most amazing experience in Kuwait. Being out in the dessert, in the middle 
		of the night, with the closest form of civilization being a hundred kilometres away. It was 
		so dark that when the car lights were turned off, I could not see my own hands in front of me. 
		It was terrifying. We were alone in the middle of the pitch-black dessert. If someone were 
		to murder us right there and then, I doubt that our bodies would ever be found. Morbid, I know, 
